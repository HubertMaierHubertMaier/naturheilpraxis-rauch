## ⚠️ Sicherheit & Kontraindikationen

| Risiko | Hinweis |
|---|---|
| **Leberbelastung** | Ab mehr als 6 Wochen: Mariendistel (Silymarin) begleitend |
| **Herxheimer-Reaktion** | Moegliche Symptomverschlimmerung - Dosis halbieren/pausieren |
| **Gallenblasenerkrankung** | Curcuma vorsichtig dosieren |
| **Blutgerinnung** | Curcuma NICHT mit Antikoagulantien |
| **Schwangerschaft/Stillzeit** | Artemisia und Gewuerznelke kontraindiziert |
| **Kinder** | Nur nach individueller Ruecksprache |
| **Thujon (Wermut)** | Neurotoxisch bei Ueberdosierung – streng nach Vorgabe |

