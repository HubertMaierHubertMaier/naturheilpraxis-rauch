## 📅 Teil 2: Kombiniertes 4-Wochen-Protokoll

> ⚠️ **Dosierungen nur in der Patientenakte dokumentieren** – hier bewusst nicht aufgefuehrt. Individuelle Anpassung je nach Konstitution, Begleitmedikation und Vertraeglichkeit.

### Phase 1: Einschleichen (Woche 1)

- Mannayan Curcu Forte+ (64 % Curcuminoide) – halbe Zieldosis
- Fenchel-Tee
- Gewuerznelke (Pulver/Kapseln) – niedrig dosiert
- Artemisia absinthium (Wermut-Tinktur) – 1x taeglich

### Phase 2: Volldosis (Woche 2–4)

- Mannayan Curcu Forte+ – Zieldosis
- Fenchel – regelmaessig
- Gewuerznelke – erhoehte Frequenz
- Artemisia absinthium – 2x taeglich
- Houttuynia cordata (optional)

### Alternative: Tinktur-Mischung (3x taeglich in Wasser)

- Curcuma-Tinktur (1:5)
- Foeniculum (Oel-Tinktur)
- Artemisia absinthium
- Cryptolepis (optional)

### Lokale Anwendung (vaginale Trichomonaden)

- **Sitzbad** (2–3x/Woche): Fenchel-Tee + Nelkenoel in Kokosoel verduennt
- **Tampon-Oel** (optional, max. 30 Min): Kokos-/Jojobaoel mit verduenntem Nelkenoel

